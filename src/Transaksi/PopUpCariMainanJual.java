    protected void datatable() {
        Object[] Baris = {"ID Mainan", "Nama Mainan", "Harga Jual", "Stok"};
        tabmode = new DefaultTableModel(null, Baris);
        tblMainanJualPopUp.setModel(tabmode);
        String cariitem = txtCariMainanJualPopUp.getText();
        try {
            String sql = "SELECT id_mainan, nama_mainan, harga_jual, stok FROM mainan "
                       + "WHERE (id_mainan LIKE ? OR nama_mainan LIKE ?) AND stok > 0 " // Hanya tampilkan mainan yang ada stok
                       + "ORDER BY id_mainan ASC";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, "%" + cariitem + "%");
            stat.setString(2, "%" + cariitem + "%");   
            ResultSet hasil = stat.executeQuery();
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString("id_mainan"),
                    hasil.getString("nama_mainan"),
                    df.format(hasil.getDouble("harga_jual")),
                    hasil.getInt("stok")
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data mainan gagal ditampilkan: " + e);
            e.printStackTrace();
        }
    }
