    private void btnPilihMainanJualPopUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihMainanJualPopUpActionPerformed
     int barisTerpilih = tblMainanJualPopUp.getSelectedRow();
        if (barisTerpilih == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih mainan terlebih dahulu.");
            return;
        }      
        if (frmPenjualan != null) {
            frmPenjualan.idMainanJualTerpilih = tblMainanJualPopUp.getValueAt(barisTerpilih, 0).toString();
            frmPenjualan.namaMainanJualTerpilih = tblMainanJualPopUp.getValueAt(barisTerpilih, 1).toString();
            try {
                String sqlDataAsli = "SELECT harga_jual, stok FROM mainan WHERE id_mainan = ?";
                PreparedStatement statDataAsli = conn.prepareStatement(sqlDataAsli);
                statDataAsli.setString(1, frmPenjualan.idMainanJualTerpilih);
                ResultSet rsDataAsli = statDataAsli.executeQuery();
                if (rsDataAsli.next()) {
                    frmPenjualan.hargaJualTerpilih = rsDataAsli.getDouble("harga_jual");
                    frmPenjualan.stokSaatIniTerpilih = rsDataAsli.getInt("stok");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal mengambil detail data mainan: " + e);
                frmPenjualan.hargaJualTerpilih = 0;
                frmPenjualan.stokSaatIniTerpilih = 0;
            }    
            frmPenjualan.mainanJualTerpilih();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error: Referensi ke Form Penjualan tidak valid!");
        }
    }//GEN-LAST:event_btnPilihMainanJualPopUpActionPerformed

