    private void btnPilihMainanStokPopUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihMainanStokPopUpActionPerformed
    int barisTerpilih = tblMainanStokPopUp.getSelectedRow();
        if (barisTerpilih == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih mainan terlebih dahulu dari tabel.");
            return;
        }
        if (frmPengelolaanStok != null) {
            frmPengelolaanStok.idMainanTerpilihStok = tblMainanStokPopUp.getValueAt(barisTerpilih, 0).toString();
            frmPengelolaanStok.namaMainanTerpilihStok = tblMainanStokPopUp.getValueAt(barisTerpilih, 1).toString();
            frmPengelolaanStok.stokAwalMainanTerpilih = Integer.parseInt(tblMainanStokPopUp.getValueAt(barisTerpilih, 2).toString());
            frmPengelolaanStok.mainanUntukPenyesuaianTerpilih(); 
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error: Referensi ke Form Pengelolaan Stok tidak valid atau belum di-set!");
        }
    }//GEN-LAST:event_btnPilihMainanStokPopUpActionPerformed

