    protected void datatable() {
        Object[] Baris = {"ID Mainan", "Nama Mainan", "Stok Saat Ini"};
        tabmode = new DefaultTableModel(null, Baris);
        tblMainanStokPopUp.setModel(tabmode); 
        String cariitem = txtCariMainanStokPopUp.getText();
        try {
            String sql = "SELECT id_mainan, nama_mainan, stok FROM mainan "
                       + "WHERE id_mainan LIKE ? OR nama_mainan LIKE ? "
                       + "ORDER BY id_mainan ASC";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, "%" + cariitem + "%");
            stat.setString(2, "%" + cariitem + "%");
            ResultSet hasil = stat.executeQuery();
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString("id_mainan"),
                    hasil.getString("nama_mainan"),
                    hasil.getInt("stok") 
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data mainan gagal ditampilkan untuk PopUp Stok: " + e);
            e.printStackTrace();
        }
    }
