     protected void datatable() {
        Object[] Baris = {"ID Mainan", "Nama Mainan", "Harga Beli", "Stok"};
        tabmode = new DefaultTableModel(null, Baris);
        if (tblMainanPopUp == null) { 
            System.err.println("tblMainanPopUp belum diinisialisasi!");
            return;
        }
        tblMainanPopUp.setModel(tabmode);
        String cariitem = (txtCariPopUp != null) ? txtCariPopUp.getText() : "";
        if (this.idSupplierFilter == null || this.idSupplierFilter.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Supplier belum dipilih untuk filter mainan.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            return; 
        }
        try {
            String sql = "SELECT id_mainan, nama_mainan, harga_beli, stok FROM mainan "
                       + "WHERE id_supplier = ? AND (id_mainan LIKE ? OR nama_mainan LIKE ?) "
                       + "ORDER BY id_mainan ASC";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, this.idSupplierFilter);
            stat.setString(2, "%" + cariitem + "%");
            stat.setString(3, "%" + cariitem + "%");
            ResultSet hasil = stat.executeQuery();
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString("id_mainan"),
                    hasil.getString("nama_mainan"),
                    df.format(hasil.getDouble("harga_beli")),
                    hasil.getInt("stok")
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data mainan gagal ditampilkan: " + e);
            e.printStackTrace();
        }
    }

