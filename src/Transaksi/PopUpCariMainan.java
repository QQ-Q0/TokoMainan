    private void btnPilihMainanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihMainanActionPerformed
         int barisTerpilih = tblMainanPopUp.getSelectedRow();
        if (barisTerpilih == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih mainan terlebih dahulu.");
            return;
        }
        if (frmPembelian != null) {
            frmPembelian.idMainanTerpilih = tblMainanPopUp.getValueAt(barisTerpilih, 0).toString();
            frmPembelian.namaMainanTerpilih = tblMainanPopUp.getValueAt(barisTerpilih, 1).toString();
            try {
                String sqlHarga = "SELECT harga_beli FROM mainan WHERE id_mainan = ?";
                PreparedStatement statHarga = conn.prepareStatement(sqlHarga);
                statHarga.setString(1, frmPembelian.idMainanTerpilih);
                ResultSet rsHarga = statHarga.executeQuery();
                if (rsHarga.next()) {
                    frmPembelian.hargaBeliTerpilih = rsHarga.getDouble("harga_beli");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal mengambil harga beli: " + e);
                frmPembelian.hargaBeliTerpilih = 0; 
            }
            frmPembelian.mainanTerpilih();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error: Referensi ke Form Pembelian tidak valid!");
        }
    }//GEN-LAST:event_btnPilihMainanActionPerformed

