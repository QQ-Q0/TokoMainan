    private void btnSimpan1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpan1ActionPerformed
        String jk = rlaki.isSelected() ? "Laki-laki" : rperempuan.isSelected() ? "Perempuan" : "";
        try {
            String sql = "INSERT INTO pelanggan (id_pelanggan, nama_pelanggan, alamat, no_telp, jenis_kelamin) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtIdPelanggan.getText());
            stat.setString(2, txtNamaPelanggan.getText());
            stat.setString(3, txtAlamat.getText());
            stat.setString(4, txtNoTelepon.getText());
            stat.setString(5, jk);
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            kosong();
            txtIdPelanggan.requestFocus();
            datatable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan: " + e);
        }
    }//GEN-LAST:event_btnSimpan1ActionPerformed

    private void btnEdit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdit1ActionPerformed
        String jk = rlaki.isSelected() ? "Laki-laki" : rperempuan.isSelected() ? "Perempuan" : "";
        try {
            String sql = "UPDATE pelanggan SET nama_pelanggan=?, jenis_kelamin=?, alamat=?, no_telp=? WHERE id_pelanggan=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtNamaPelanggan.getText());
            stat.setString(2, jk);
            stat.setString(3, txtAlamat.getText());
            stat.setString(4, txtNoTelepon.getText());
            stat.setString(5, txtIdPelanggan.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            kosong();
            datatable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal diubah: " + e);
        }
    }//GEN-LAST:event_btnEdit1ActionPerformed

    private void btnHapus1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapus1ActionPerformed
        int ok = JOptionPane.showConfirmDialog(null, "Hapus?", "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            try {
                String sql = "DELETE FROM pelanggan WHERE id_pelanggan=?";
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, txtIdPelanggan.getText());
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                kosong();
                datatable();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal dihapus: " + e);
            }
        }
    }//GEN-LAST:event_btnHapus1ActionPerformed

