    protected void datatable(){
    Object[] Baris = {"ID Pelanggan", "Nama", "Jenis Kelamin", "Alamat", "No Telepon"};
    tabmode = new DefaultTableModel(null, Baris);
    tablePelanggan.setModel(tabmode);
    String cariItem = txtCari.getText();
    try {
        String sql = "SELECT * FROM pelanggan WHERE id_pelanggan LIKE ? OR nama_pelanggan LIKE ? ORDER BY id_pelanggan ASC";      
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, "%" + cariItem + "%");
        stat.setString(2, "%" + cariItem + "%");       
        ResultSet hasil = stat.executeQuery();
        while (hasil.next()) {
            tabmode.addRow(new Object[]{
                hasil.getString("id_pelanggan"),
                hasil.getString("nama_pelanggan"),
                hasil.getString("jenis_kelamin"),
                hasil.getString("alamat"),
                hasil.getString("no_telp")
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Data gagal ditampilkan: " + e);
        e.printStackTrace();
    }
}

    protected void autonumberIdPelanggan() {
    try {
        String sql = "SELECT id_pelanggan FROM pelanggan ORDER BY id_pelanggan DESC LIMIT 1";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);     
        txtIdPelanggan.setText("P001");      
        if (rs.next()) {
            String id_pelanggan_terakhir = rs.getString("id_pelanggan").substring(1); 
            int AN = Integer.parseInt(id_pelanggan_terakhir) + 1;
            String Nol = "";
            if (AN < 10) { Nol = "00"; }
            else if (AN < 100) { Nol = "0"; }
            txtIdPelanggan.setText("P" + Nol + AN);
        }
    } catch (Exception e) { 
        JOptionPane.showMessageDialog(null, "Auto Number ID Pelanggan Gagal: " + e);
        e.printStackTrace();
    }
}
