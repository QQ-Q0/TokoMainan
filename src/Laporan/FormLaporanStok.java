    private void btnTampilkanLapStokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTampilkanLapStokActionPerformed
        try {
            Connection reportConn = new koneksi().connect();
            String pathJasper = "/laporan/LaporanStokMainan.jasper";
            InputStream jasperStream = getClass().getResourceAsStream(pathJasper);
            if (jasperStream == null) {
                JOptionPane.showMessageDialog(this, "File LaporanStokMainan.jasper tidak ditemukan di: " + pathJasper, "Error File", JOptionPane.ERROR_MESSAGE);
                return;
            }
            HashMap<String, Object> parameter = new HashMap<>();
            String idKategoriDipilih = null;
            if (cmbKategoriLapStok.getSelectedIndex() > 0) {
                String itemTerpilih = cmbKategoriLapStok.getSelectedItem().toString();
                idKategoriDipilih = itemTerpilih.split(" - ")[0];
            }
            parameter.put("P_ID_KATEGORI", idKategoriDipilih == null ? "" : idKategoriDipilih);
            Integer batasStokKritis = null;
            if (j1.isSelected()) {
                try {
                    batasStokKritis = Integer.parseInt(txtBatasStokKritis.getText());
                    if (batasStokKritis < 0) {
                        JOptionPane.showMessageDialog(this, "Batas stok kritis tidak boleh negatif.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                        txtBatasStokKritis.requestFocus();
                        return;
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Batas stok kritis harus berupa angka.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                    txtBatasStokKritis.requestFocus();
                    return;
                }
            }
            parameter.put("P_BATAS_STOK_KRITIS", batasStokKritis == null ? -1 : batasStokKritis);
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperStream, parameter, reportConn);
            if (jasperPrint.getPages().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tidak ada data stok yang sesuai dengan filter yang dipilih.", "Informasi Laporan", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JasperViewer.viewReport(jasperPrint, false);
            }
        } catch (JRException e) {
            JOptionPane.showMessageDialog(this, "Gagal menampilkan laporan stok: " + e.getMessage(), "Error Laporan", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan umum: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnTampilkanLapStokActionPerformed

