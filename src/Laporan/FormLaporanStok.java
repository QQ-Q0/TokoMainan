        private void loadKategori() {
            cmbKategoriLapStok.removeAllItems();
            cmbKategoriLapStok.addItem("-- Semua Kategori --"); 
    try {
        String sql = "SELECT id_kategori, nama_kategori FROM kategori_mainan ORDER BY id_kategori ASC";
        Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);
        while (hasil.next()) {
            cmbKategoriLapStok.addItem(hasil.getString("id_kategori") + " - " + hasil.getString("nama_kategori"));
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Gagal memuat data kategori: " + e);
        e.printStackTrace();
    }
}

